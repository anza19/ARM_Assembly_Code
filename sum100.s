		.text
		.global _start

_start:	
		MOV R0, #0   // SUM AT THE START = 0
		MOV R1, #101 //LENGTH WE WANT TO ADD TO
		MOV R2, #0   //VARIABLE i STARTS AT 0

FOR_LOOP:
		ADD R0, R2, R0  //ADD CONTENTS OF R0 AND R2, STORE IN R0
		ADD R2, R2, #1  //INCREMENT R2
		CMP R2, R1      //CHECK IF R2 EQUALS 100
		BEQ DONE
		B FOR_LOOP

DONE: B DONE
